import UIKit

extension UIColor {
	static func themePrimaryColor() -> UIColor {
		return UIColor(red: 0.2, green: 0.69, blue: 0.76, alpha: 1);
	}
	
	static func themeTintColor() -> UIColor {
		return UIColor.whiteColor();
	}
	
	static func themeNavBarTitleColor() -> UIColor {
		return UIColor.whiteColor();
	}
	
}
